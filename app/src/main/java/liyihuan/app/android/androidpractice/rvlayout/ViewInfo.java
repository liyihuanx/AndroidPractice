package liyihuan.app.android.androidpractice.rvlayout;

import android.view.View;

/**
 * @author created by liyihuanx
 * @date 2021/1/8
 * description: 类的描述
 */
public class ViewInfo {

    private int top;
    private int bottom;
    private int left;
    private int right;
    private View view;

    public int getTop() {
        return top;
    }

    public void setTop(int top) {
        this.top = top;
    }

    public int getBottom() {
        return bottom;
    }

    public void setBottom(int bottom) {
        this.bottom = bottom;
    }

    public int getLeft() {
        return left;
    }

    public void setLeft(int left) {
        this.left = left;
    }

    public int getRight() {
        return right;
    }

    public void setRight(int right) {
        this.right = right;
    }

    public View getView() {
        return view;
    }

    public void setView(View view) {
        this.view = view;
    }
}
